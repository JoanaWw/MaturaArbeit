         Militärstützpunkte & Feuerwehrübungsplätze
In diesem Kapitel werden zwei bekannte Hauptquellen der PFOS-Belastung zusammengefasst. 
Auf Militärstützpunkten und Feuerwehrübungsplätzen wird häufig PFOS-haltiger Feuerlöschschaum verwendet, der dann bei Übungen in die Umwelt gelangt. 
Das Beispiel der Stadt Ronneby in Schweden verdeutlicht dies eindrucksvoll. 
Leider oder glücklicherweise gibt es bisher keine Dokumentation eines solchen vergleichbaren Falls in der Schweiz. 
Im Folgenden wird auf den Fall Ronneby näher eingegangen. 
Ronneby ist eine kleine Gemeinde in Südschweden. 
Im September 2013 wurde dort im Grundwasser eine extrem hohe Konzentration verschiedener PFAS, einschliesslich PFOS, gemessen.  
Daraufhin wurden die beiden Trinkwasserversorger Brantafors und Kärragarden der Gemeinde genauer untersucht.  
Diese Messungen ergaben sehr hohe Konzentrationen von PFOS beim Trinkwasserversorger Brantafors.  
In der folgenden Tabelle sind die im Dezember 2013 gemessenen PFOS-Werte der beiden Trinkwasserversorger und als Vergleichswert die PFOS-Werte der Nachbargemeinde Karlshamn aufgeführt.  
Karlshamm liegt circa 30 Kilometer entfernt von Ronneby und der Trinkwasserversorger dieser Gemeinde dient als Vergleichswert.

Tabelle 2: Eigene Darstellung der PFOS Konzentration im Trinkwasser von drei verschiedenen Trinkwasserversorgern. 
Dargestellt ist hier das arithmetische Mittel im Trinkwasser.   
(siehe Einschub: Erklärung der statistischen Kennzahlen S. 23.) 
In der Schweiz ist der derzeitige Grenzwert für PFOS in Trinkwasser 300 ng/l. 
Der PFOS-Gehalt in Brantafors würde somit deutlich über dem Grenzwert der Schweiz liegen.

So wurden in Brantafors 8 000 ng/l PFOS im Trinkwasser gefunden, das an die Gemeinde verteilt wurde.
Im Vergleich dazu sind es in Karlshamn weniger als 0,2 ng/l Trinkwasser. 
Diese Daten zeigen, dass die PFOS-Konzentration beim Trinkwasser-versorger Brantafors fast 300-mal höher ist als in Kärragarden und etwa 40 000-mal höher als in der Nachbargemeinde Karlshamn.
Diese alarmierenden Werte führten dazu, dass die Wasserversorgung von Brantafors sofort geschlossen wurde.  
Als Ursache der PFOS-Kontamination wurde ein Militärstützpunkt bestätigt, der nur 2 km ausserhalb von Ronneby entfernt liegt. 
Auf dieser Militärbasis wurde seit den 80er Jahren Feuerlöschschaum mit AFFF verwendet, der eine Mischung aus vielen PFAS-Chemikalien - inklusive PFOS – enthielt. 
Etwa ein Drittel der 12 000 Einwohner von Ronneby wurde unwissentlich über viele Jahre mit PFOS-belastetem Trinkwasser versorgt. 
Als die hohen PFOS-Werte im Dezember 2013 bekannt wurden, reagierten die schwedischen Behörden sehr schnell. 
Im Jahr 2014 wurde ein neuer Grenzwert von 90 ng/l für sieben PFAS, einschliesslich PFOS, im Trinkwasser Schwedens festgelegt.  
Zum anderen wurden bereits im Februar 2014 Blutuntersuchungen in zwei Schulklassen mit 11-jährigen Schülern und Schülerinnen durchgeführt.  
Eine Klasse aus dem von PFOS belasteten Gebiet in Ronneby (Brantafors) und eine aus dem nicht betroffenen Gebiet in Ronneby (Kärragarden) wurden getestet. 
Nachdem bei allen 20 Schülern und Schülerinnen, die mit dem belasteten Trinkwasser in Brantafors aufgewachsen waren, erhöhte PFOS-Werte festgestellt wurden, wurde der gesamten Bevölkerung kostenlose Bluttests angeboten.  
Es wurde eine aufwendige Untersuchung mit anschliessenden komplexen statistischen Verfahren durchgeführt, deren verkürzte Ergebnisse für PFOS in der nachstehenden Tabelle zusammengefasst sind. 

Tabelle 3: Eigene Darstellung. Es werden hier tabellarisch die drei untersuchten Gruppen aufgeführt. 
Alle Werte beziehen sich auf die PFOS-Konzentration der jeweiligen Gruppe im Blut der Studienteilnehmer.   
Eine detailliertere Beschreibung findet in den nächsten Absätzen statt.

Einschub: Erklärung der statistischen Kennzahlen.
In der Tabelle 3 werden vier statistische Kennzahlen aufgeführt. 
Das Minimum (Min) und das Maximum (Max) sind der Kleinste und der Höchste gemessene Wert der Datenreihe. 
In der Gruppe Brantafors gab es also eine Person mit einem PFOS-Wert von 24,1 µg/l Blut und eine Person mit 1 500 µg/l  Blut. 
Das arithmetische Mittel (Arith. Mittel) liegt bei 387 µg/l mit einer Standardabweichung (SD) von 259 µg/l Blut.  
Das arithmetische Mittel oder der Durchschnitt wird berechnet, indem alle gemessenen Werte summiert werden und danach die Summe durch die Anzahl der Werte dividiert wird. 
Als Mass für die Streuung der Stichprobe um diesen Wert wird die Standardabweichung berechnet, d.h. die Wurzel aus dem mittleren quadrierten Abstand der Messwerte von diesem arithmetischen Mittel: Die Standardabweichung ist mit 259 µg/l als relativ hoch anzusehen, d.h. es wird eine breite Verteilung der Messungen um den arithmetischen Mittelwert gesehen. 

Interpretation der Tabelle 3:
Für die Untersuchung wurden die freiwilligen Teilnehmer in drei Gruppen eingeteilt. 
Alle Teilnehmer der Gruppe Brantafors wurde mit dem PFOS belasteten Trinkwasser versorgt. 
In dieser Gruppe nahmen 106 Personen an der Studie teil. Aus 3 418 Personen setzt sich die Gruppe Ronneby zusammen. 
Diese Gruppe bezog ihr Trinkwasser von beiden Trinkwasserversorgern Brantafors und Kärragarden in Ronneby. 
Die Gruppe Karlshamn hat ihr Trinkwasser von dem Trinkwasserversorger Karlshamn bezogen und hier haben sich 242 Personen für freiwillige Bluttests gemeldet.
Die PFOS-Belastung der drei Gruppen wird anhand des arithmetischen Mittels im folgendem genauer beschrieben. 
In der Gruppe Brantafors haben die Studien-teilnehmer die höchste PFOS-Konzentration in ihrem Blut. 
Ihr arithmetisches Mittel liegt bei 387 µg/l. Das arithmetische Mittel der Gruppe Ronneby beträgt 245 µg/l. 
Der Wert liegt etwas niedriger im Vergleich zur Gruppe Brantafors, da diese Gruppe das Trinkwasser von beiden Trinkwasserversorgern bekommen hat. 
Somit gibt es Personen, die nicht mit dem PFOS-belasteten Trinkwasser in ihren Häusern versorgt wurden. 
Trotzdem ist das arithmetische Mittel immer noch viel höher als in der Gruppe Karlshamn, die sauberes Trinkwasser hatte.  
Ihr arithmetisches Mittels liegt nur bei 5,68 µg/l. 
Die PFOS-Konzentration der Gruppe Brantafors ist etwa 68-mal höher als in der Gruppe Karlshamn – gemessen am arithmetischen Mittel. 
Vergleicht man das arithmetische Mittel der Gruppe Brantafors von 387 µg/l mit dem HBM-II Wert von 20 µg/l, so stellt man folgendes fest: Die PFOS-Konzentration der Gruppe Brantafors ist circa 20-fach höher ist als der HBM-II Wert.
Interessant ist, dass die Konzentration im Trinkwasser von Ronneby bei 8 000 ng/l lag. 
Das entspricht 8 µg/l. Die Konzentration im Trinkwasser scheint viel niedriger zu sein als die Konzentration im Blut, die 387 µg/l in der Gruppe Ronneby beträgt. 
Eine Erklärung dafür wäre, dass sich die Chemikalie im Laufe der Zeit im Körper anreichert. 
Leider gibt es dazu noch keine wissenschaftlichen Untersuchungen.
Im Kapitel «Mensch» wird ebenfalls detailliert darauf eingegangen, dass 3,6% der Teilnehmer der Pilotstudie in der Schweiz PFOS-Werte über dem HBM-II-Wert aufwiesen. Dies entspricht etwa 28 Personen von insgesamt 789 Teilnehmern, deren arithmetisches Mittel bei 7,8 µg/l lag.  Im Vergleich dazu lag das arithmetische Mittel in Karlshamn bei nur 5,68 µg/l, was darauf hindeutet, dass die Schweizer Bevölkerung weit weniger PFOS-Exposition ausgesetzt ist als die von Ronneby.
Ronneby gilt als einer der am stärksten von PFOS belasteten Orte weltweit. 
Angesichts der dramatischen Auswirkungen wurde umfangreiche Forschung zu diesem Fall durchgeführt. 
Die schwedischen Behörden schlossen umgehend die betroffenen Wasserversorger und führten umfassende Untersuchungen durch. 
Dieser Fall verdeutlicht, wie weitreichend die Auswirkungen von PFOS-Kontaminationen sein können, wenn die Chemikalie unbemerkt über Böden und Grundwasser in das Trinkwasser und damit in den menschlichen Körper gelangt.


   
