        <h3>Mögliche Beeinflussung des Wassers</h3>
        Für die Untersuchung ist es nicht nur relevant zu wissen, woher das Wasser stammt, sondern auch, ob es möglicherweise beeinflusst wurde. 
        Die Proben wurden in vier verschiedene Kategorien eingeteilt: Landwirtschaft, Siedlung/Industrie, Fassung mit Risikopotenzial oder keine Beeinflussung.
        Eine Beeinflussung des Wassers durch Landwirtschaft oder Siedlungen/Industrie ist relativ eindeutig festzustellen. 
        Wenn eine Wasserprobe mit Risikopotenzial vorliegt, bedeutet dies, dass dieses Gewässer durch einen potenziell belasteten Ort wie einen Brandplatz, eine Deponie oder einen Militärstützpunkt beeinflusst wurde. 
