        <h3 id="origin">Ursprung des Wassers</h3>
        Das Trinkwasser in der Schweiz stammt aus verschiedenen Quellen. 
        Grundwasser stellt mit 213 Wasserproben (38%) den grössten Anteil dar, gefolgt von Quellwasser mit 31%. 
        26% der Proben stammen aus Mischwasser unterschiedlicher Herkunft, während nur 5% der Proben aus Oberflächen- oder Seewasser entnommen wurden. 

