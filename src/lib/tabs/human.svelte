            Ergebnisse der Pilotstudie:  
            In der Schweiz wurden noch keine Grenzwerte für PFOS im menschlichen Körper bestimmt.
            Daher wurden in der vorliegenden Pilotstudie die HBM-I und HBM-II Grenzwerte des deutschen Bundesumweltamtes für die Allgemeinbevölkerung angewendet.
            PFOS wurde in allen Blutproben nachgewiesen. 
            Bei 3,6% der Studienteilnehmenden lag der PFOS-Wert oberhalb des HBM-II-Wertes von 20 µg/l, einem Wert, ab dem gesundheitliche Beeinträchtigungen möglich sind. 
            Männer waren stärker belastet als Frauen: 4,9% der Männer, verglichen mit 2,5% der Frauen, hatten PFOS-Konzentrationen über dem HBM-II-Wert. 
            Vermutlich hängt dieser Unterschied mit dem schnelleren PFOS-Abbau bei Frauen durch Menstruationsblut oder Geburten zusammen.
            Ein nächster Schritt wird darin bestehen, die Quellen der PFOS-Konzentrationen im menschlichen Körper zu identifizieren, 
            einschliesslich möglicher Expositionen durch Nahrung, Kosmetika, Haushaltsreiniger oder berufliche Tätigkeiten.  
            Ergebnisse werden erst in den nächsten Jahren erwartet.
            Interessant ist auch eine Auswertung der deutschen Umweltprobenbank, die die PFOS-Belastung im Blut von Studierenden im Zeitverlauf untersuchte.<br/><br/>
            
