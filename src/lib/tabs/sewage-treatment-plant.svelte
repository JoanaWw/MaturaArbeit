        PFOS-haltige Produkte werden häufig nach Gebrauch entsorgt und können dann auf Deponien oder in Kläranlagen landen. 
        Derzeit gibt es kein standardisiertes Verfahren, dass PFOS aus dem Abwasser oder dem Klärschlamm herausfiltern kann. 
        Somit landet die Chemikalie im Abwasser oder im Klärschlamm. Auf diesem Weg kann die Chemikalie in den Boden oder ins Grundwasser gelangen. 
        Der Kanton St. Gallen überprüfte im Jahr 2021 insgesamt 41 Kläranlagen auf PFOS. Dabei wurde sowohl die Zufuhr als auch der Abfluss der Kläranlagen untersucht. 
        In 40 von 41 Proben der Zuläufe wurde PFOS gefunden, und auch in 39 Proben der Abflüsse wurde die Chemikalie gefunden. Für Zürich liegen leider noch keine derartigen Untersuchungen vor. 
        Derzeit läuft jedoch ein gesamtschweizerisches Projekt zur Untersuchung des Vorkommens von PFAS in Schweizer Kläranlagen. 
        Ergebnisse werden für 2025 erwartet.<br/><br/>
        
