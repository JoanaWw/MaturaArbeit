    <h3>Studie zur PFOS Belastung im Kanton St. Gallen</h3>
    Im Kanton St. Gallen wurden 2021 erstmals Untersuchungen zur PFOS-Belastung in Fischen durchgeführt. Insgesamt wurden je fünf Bachforellen aus zehn verschiedenen Gewässern untersucht.  Neun der untersuchten Gewässer wiesen bereits eine erhöhte PFOS-Belastung auf.  Ein weiteres Gewässer diente als Referenz. Es wurde sowohl Fischfilet als auch Fischleber untersucht, wobei in allen Proben PFOS nachgewiesen werden konnte.  
    <br/><br/>
    Die Ergebnisse sind in der folgenden Abbildung dargestellt.

    <TxtImage src="fish3.png">
        Die Abbildung zeigt die PFOS-Konzentration in den Bachforellenfilets und den Fliessgewässern. 
        Die höchste gemessene PFOS-Konzentration wurde im Dorfbach von Goldach festgestellt und betrug 2011 ng/l, was ausserhalb des dargestellten Konzentrationsbereichs liegt.  
        Es ist bekannt, dass die Goldach durch den Einsatz von Feuerlöschschaum, der PFOS enthielt, kontaminiert wurde. 
        Die dicken horizontalen Balken zeigen die Median-Werte  der Messreihe. 
        Der gesetzliche Grenzwert für PFOS in Forellen liegt bei 2 µg/kg und wird in allen Messwerten überschritten.
    </TxtImage>

    Es wäre auch für zukünftige Forschungsarbeiten interessant, den Zusammenhang zwischen der Belastung von Gewässern und Fischen genauer zu untersuchen. 
    Die beiden dargestellten Einheiten, µg/kg für die PFOS-Konzentration im Fischfilet und ng/l für die Konzentration in den Gewässern, lassen sich nicht unmittelbar vergleichen. 
    Betrachtet man beispielsweise den Fluss Sitter, so fällt auf, dass das Gewässer fast keine PFOS-Konzentration aufweist, während die untersuchten Fische sehr wohl belastet sind. 
    Dies könnte darauf hindeuten, dass sich PFOS über die Zeit im Fisch anreichert und die Flüsse besser durchmischt werden.
    Interessant ist in diesem Zusammenhang auch das Ergebnis des Kapitels Militärstützpunkte und Feuerwehrübungsplätze (Ronneby). 
    Auch hier wurde festgestellt, dass die Konzentration im Trinkwasser relativ niedrig und die Konzentration im menschlichen Blut relativ hoch ist. 
    Ähnlich scheint es hier bei den Fischen zu sein.
    Zusammenfassend kann an dieser Stelle festgehalten werden, dass in allen untersuchten Flüssen und Fischen in der Studie PFOS - zum Teil in sehr hohen Konzentrationen – nachgewiesen wurde. 

